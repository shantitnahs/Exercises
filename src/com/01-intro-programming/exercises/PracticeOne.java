/*
    Declare and initialize all the possible primitives in Java and print it
 */
class PracticeOne {

    public static void main(String[] args) {
                // boolean
                boolean flag = true;
                System.out.println("Boolean: " + flag);

                // byte
                byte range = 124;
                System.out.println("Byte: " + range);

                // short
                short temperature = -200;
                System.out.println("Short: " + temperature);

                // int
                int number = -4250000;
                System.out.println("Int: " + number);

                // long
                long rangeL = -42332200000L;
                System.out.println("Long: " + rangeL);

                // float
                float numberF = -42.3f;
                System.out.println("Float: " + numberF);

                // double
                double numberD = -42.3;
                System.out.println("Double: " + numberD);

                // char
                char letter = '\u0051';
                System.out.println("Char: " + letter);
            }
        }